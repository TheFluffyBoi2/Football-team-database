CREATE OR REPLACE FUNCTION E8(v_nume_jucator JUCATOR.NUME%TYPE) RETURN NUMBER
IS 
    v_numar_meciuri_acasa NUMBER;
    v_numar_meciuri_deplasare NUMBER;
    v_meciuri_totale NUMBER;
    v_id_club JUCATOR.ID_CLUB%TYPE;
    v_id_jucator JUCATOR.ID_JUCATOR%TYPE;
    NU_ARE_ECHIPA EXCEPTION;
BEGIN
    BEGIN
        SELECT ID_JUCATOR, ID_CLUB INTO v_id_jucator, v_id_club
        FROM JUCATOR
        WHERE NUME = v_nume_jucator;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RAISE_APPLICATION_ERROR(-20001, 'Jucatorul nu exista');
        WHEN TOO_MANY_ROWS THEN
            RAISE_APPLICATION_ERROR(-20002, 'Exista mai multi jucatori cu acelasi nume');
    END;

    IF v_id_club IS NULL THEN
        RAISE NU_ARE_ECHIPA;
    END IF;

    SELECT COUNT(*) INTO v_numar_meciuri_acasa
    FROM MECI M
    JOIN CLUB_DE_FOTBAL CDF ON M.ID_CLUB_ACASA = CDF.ID_CLUB
    JOIN JUCATOR J ON J.ID_CLUB = CDF.ID_CLUB OR J.ID_CLUB = CDF.ID_CLUB
    WHERE J.NUME = v_nume_jucator 
    AND TO_NUMBER(SUBSTR(M.REZULTAT, 1, INSTR(M.REZULTAT, '-') - 1)) > TO_NUMBER(SUBSTR(M.REZULTAT, INSTR(M.REZULTAT, '-') + 1));
    
    SELECT COUNT(*) INTO v_numar_meciuri_deplasare
    FROM MECI M
    JOIN CLUB_DE_FOTBAL CDF ON M.ID_CLUB_DEPLASARE = CDF.ID_CLUB
    JOIN JUCATOR J ON J.ID_CLUB = CDF.ID_CLUB OR J.ID_CLUB = CDF.ID_CLUB
    WHERE J.NUME = v_nume_jucator 
    AND TO_NUMBER(SUBSTR(M.REZULTAT, 1, INSTR(M.REZULTAT, '-') - 1)) < TO_NUMBER(SUBSTR(M.REZULTAT, INSTR(M.REZULTAT, '-') + 1));
    
    v_meciuri_totale := v_numar_meciuri_acasa + v_numar_meciuri_deplasare;

    RETURN v_meciuri_totale;
EXCEPTION
    WHEN NU_ARE_ECHIPA THEN
        RAISE_APPLICATION_ERROR(-20003, 'Jucatorul nu are echipa');
    WHEN OTHERS THEN
        RAISE_APPLICATION_ERROR(-20004, 'Alte erori: ' || SQLERRM);
END E8;
/

