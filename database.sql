CREATE OR REPLACE TRIGGER E11
    BEFORE UPDATE OF SALARIU ON JUCATOR
    FOR EACH ROW
DECLARE
    v_salariu_maxim JUCATOR.SALARIU%TYPE;
    v_nota STATISTICA.NOTA%TYPE;
    exceptie EXCEPTION;
BEGIN
    SELECT NOTA INTO v_nota
    FROM STATISTICA
    WHERE ID_JUCATOR = :OLD.ID_JUCATOR;

    CASE
        WHEN v_nota <= 7 THEN v_salariu_maxim := 100000;
        WHEN v_nota <= 9 AND v_nota > 7 THEN v_salariu_maxim := 250000;
        WHEN v_nota <= 10 AND v_nota > 9 THEN v_salariu_maxim := 400000;
    END CASE;

    IF (:NEW.SALARIU > v_salariu_maxim) AND (TRUNC(MONTHS_BETWEEN(SYSDATE, :OLD.DATA_NASTERII) / 12) >= 30) THEN
        RAISE exceptie;
    END IF;

EXCEPTION
    WHEN exceptie THEN
        RAISE_APPLICATION_ERROR(-20009, 'Nu se poate mari salariul');
END;
/

