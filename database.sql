CREATE SEQUENCE FAN_SEQ START WITH 1;
CREATE SEQUENCE CLUB_DE_FANI_SEQ START WITH 1;
CREATE SEQUENCE ANTRENOR_SEQ START WITH 1;
CREATE SEQUENCE CENTRU_ANTRENAMENT_SEQ START WITH 1;
CREATE SEQUENCE CLUB_DE_FOTBAL_SEQ START WITH 1;
CREATE SEQUENCE STADION_SEQ START WITH 1;
CREATE SEQUENCE MECI_SEQ START WITH 1;
CREATE SEQUENCE JUCATOR_SEQ START WITH 1;
CREATE SEQUENCE ANTRENAMENT_SEQ START WITH 1;
CREATE SEQUENCE RECUPERARE_SEQ START WITH 1;
CREATE SEQUENCE FIZIOTERAPEUT_SEQ START WITH 1;

--Tabelul CLUB_DE_FANI
CREATE TABLE CLUB_DE_FANI (
    ID_CLUB_DE_FANI INT DEFAULT CLUB_DE_FANI_SEQ.NEXTVAL PRIMARY KEY,
    NUME VARCHAR2(50) NOT NULL,
    DATA_INFIINTARE DATE
);

--Tabelul FAN
CREATE TABLE FAN (
    ID_FAN INT DEFAULT FAN_SEQ.NEXTVAL PRIMARY KEY,
    ID_CLUB_DE_FANI INT,
    NUME VARCHAR2(50) NOT NULL,
    PRENUME VARCHAR2(50) NOT NULL,
    SALARIU INT NOT NULL,
    FOREIGN KEY (ID_CLUB_DE_FANI) REFERENCES CLUB_DE_FANI(ID_CLUB_DE_FANI)
);

--Tabelul ANTRENOR
CREATE TABLE ANTRENOR (
    ID_ANTRENOR INT DEFAULT ANTRENOR_SEQ.NEXTVAL PRIMARY KEY,
    NUME VARCHAR2(50) NOT NULL,
    PRENUME VARCHAR2(50) NOT NULL,
    VARSTA INT NOT NULL,
    TIP_LICENTA VARCHAR(5),
    DURATA_CONTRACT VARCHAR2(50),
    SALARIU INT NOT NULL,
    CONSTRAINT VARSTA_CHECK CHECK (VARSTA >= 18 AND VARSTA <= 90),
    CONSTRAINT TIP_LICENTA_CHECK CHECK (UPPER(TIP_LICENTA) IN ('PRO', 'A', 'B', 'C')),
    CONSTRAINT DURATA_CONTRACT_CHECK CHECK (UPPER(DURATA_CONTRACT) LIKE '%_LUNI')
);

--Tabelul CENTRU_ANTRENAMENT
CREATE TABLE CENTRU_ANTRENAMENT (
    ID_CENTRU_DE_ANTRENAMENT INT DEFAULT CENTRU_ANTRENAMENT_SEQ.NEXTVAL PRIMARY KEY,
    NUME VARCHAR2(50),
    CAPACITATE INT NOT NULL,
    DATA_CONSTRUCTIE DATE,
    LOCATIE VARCHAR2(50) NOT NULL
);

--Tabelul STADION
CREATE TABLE STADION (
    ID_STADION INT DEFAULT STADION_SEQ.NEXTVAL PRIMARY KEY,
    NUME VARCHAR2(50) NOT NULL,
    DATA_CONSTRUCTIE DATE NOT NULL,
    CAPACITATE INT NOT NULL,
    LOCATIE VARCHAR2(50) NOT NULL,
    TIP_GAZON VARCHAR(50),
    CONSTRAINT TIP_GAZON_CHECK CHECK (UPPER(TIP_GAZON) IN ('NATURAL', 'ARTIFICIAL', 'HIBRID'))
);

--Tabelul FIZIOTERAPEUT
CREATE TABLE FIZIOTERAPEUT (
    ID_FIZIOTERAPEUT INT DEFAULT FIZIOTERAPEUT_SEQ.NEXTVAL PRIMARY KEY,
    NUME VARCHAR2(50) NOT NULL,
    PRENUME VARCHAR2(50) NOT NULL,
    DURATA_CONTRACT VARCHAR2(50),
    SALARIU INT NOT NULL,
    CONSTRAINT DURATA_FIZIO_CONTRACT_CHECK CHECK (UPPER(DURATA_CONTRACT) LIKE '%_LUNI')
);

--Tabelul CLUB_DE_FOTBAL
CREATE TABLE CLUB_DE_FOTBAL (
    ID_CLUB INT DEFAULT CLUB_DE_FOTBAL_SEQ.NEXTVAL PRIMARY KEY,
    ID_STADION INT,
    ID_ANTRENOR INT,
    ID_CLUB_DE_FANI INT,
    ID_FIZIOTERAPEUT INT,
    ID_CENTRU_DE_ANTRENAMENT INT,
    NUME VARCHAR2(50) NOT NULL,
    AN_INFIINTARE INT NOT NULL,
    LIGA VARCHAR2(50),
    PUNCTAJ INT,
    CONSTRAINT LIGA_CLUB_CHECK CHECK (UPPER(LIGA) IN ('BUNDESLIGA', 'LIGA 1', 'LIGUE 1', 'PREMIER LEAGUE', 'SERIE A', 'LA LIGA')),
    FOREIGN KEY (ID_STADION) REFERENCES STADION(ID_STADION) ON DELETE SET NULL,
    FOREIGN KEY (ID_ANTRENOR) REFERENCES ANTRENOR(ID_ANTRENOR),
    FOREIGN KEY (ID_CLUB_DE_FANI) REFERENCES CLUB_DE_FANI(ID_CLUB_DE_FANI),
    FOREIGN KEY (ID_FIZIOTERAPEUT) REFERENCES FIZIOTERAPEUT(ID_FIZIOTERAPEUT),
    FOREIGN KEY (ID_CENTRU_DE_ANTRENAMENT) REFERENCES CENTRU_ANTRENAMENT(ID_CENTRU_DE_ANTRENAMENT)
);

--Tabelul CENTRU_ANTRENAMENT_CLUB
CREATE TABLE CENTRU_ANTRENAMENT_CLUB (
    ID_CLUB INT,
    ID_CENTRU_DE_ANTRENAMENT INT,
    PRIMARY KEY (ID_CLUB, ID_CENTRU_DE_ANTRENAMENT),
    FOREIGN KEY (ID_CLUB) REFERENCES CLUB_DE_FOTBAL(ID_CLUB),
    FOREIGN KEY (ID_CENTRU_DE_ANTRENAMENT) REFERENCES CENTRU_ANTRENAMENT(ID_CENTRU_DE_ANTRENAMENT)
);

--Tabelul MECI
CREATE TABLE MECI (
    ID_MECI INT DEFAULT MECI_SEQ.NEXTVAL PRIMARY KEY,
    ID_CLUB_ACASA INT,
    ID_CLUB_DEPLASARE INT,
    REZULTAT VARCHAR2(5),
    NUMAR_FANI INT,
    FOREIGN KEY (ID_CLUB_ACASA) REFERENCES CLUB_DE_FOTBAL(ID_CLUB),
    FOREIGN KEY (ID_CLUB_DEPLASARE) REFERENCES CLUB_DE_FOTBAL(ID_CLUB)
);

--Tabelul JUCATOR
CREATE TABLE JUCATOR (
    ID_JUCATOR INT DEFAULT JUCATOR_SEQ.NEXTVAL PRIMARY KEY,
    ID_CLUB INT,
    NUME VARCHAR2(50) NOT NULL,
    PRENUME VARCHAR2(50) NOT NULL,
    DATA_NASTERII DATE NOT NULL,
    POZITIE VARCHAR2(3),
    NUMAR_TRICOU INT,
    DURATA_CONTRACT VARCHAR2(50),
    SALARIU INT NOT NULL,
    FOREIGN KEY (ID_CLUB) REFERENCES CLUB_DE_FOTBAL(ID_CLUB),
    CONSTRAINT POZITIE_CHECK CHECK (UPPER(POZITIE) IN ('GK', 'CB', 'LB', 'RB', 'CM', 'LM', 'RM', 'CDM', 'CAM', 'LW', 'RW', 'ST')),
    CONSTRAINT DURATA_CONRACT_JUCATOR_CHECK CHECK (UPPER(DURATA_CONTRACT) LIKE '%_LUNI'),
    CONSTRAINT NUMAR_TRICOU_CHECK CHECK (NUMAR_TRICOU < 100 AND NUMAR_TRICOU > 0)
);

--Tabelul STATISTICA
CREATE TABLE STATISTICA (
    ID_JUCATOR PRIMARY KEY,
    NUMAR_GOLURI INT,
    NUMAR_ASISTURI INT,
    MINUTE_JUCATE INT,
    NOTA NUMBER(5, 2),
    CONSTRAINT NOTA_CHECK CHECK (NOTA >= 1 AND NOTA <= 10),
    FOREIGN KEY (ID_JUCATOR) REFERENCES JUCATOR(ID_JUCATOR)
);

--Tabelul ANTRENAMENT
CREATE TABLE ANTRENAMENT (
    ID_ANTRENAMENT INT DEFAULT ANTRENAMENT_SEQ.NEXTVAL PRIMARY KEY,
    ID_JUCATOR INT,
    ID_CENTRU_DE_ANTRENAMENT INT,
    DURATA VARCHAR2(50),
    FOREIGN KEY (ID_JUCATOR) REFERENCES JUCATOR(ID_JUCATOR),
    FOREIGN KEY (ID_CENTRU_DE_ANTRENAMENT) REFERENCES CENTRU_ANTRENAMENT(ID_CENTRU_DE_ANTRENAMENT),
    CONSTRAINT DURATA_CHECK CHECK (UPPER(DURATA) LIKE '%_MINUTE')
);

--Tabelul RECUPERARE
CREATE TABLE RECUPERARE (
    ID_RECUPERARE INT DEFAULT RECUPERARE_SEQ.NEXTVAL PRIMARY KEY,
    ID_FIZIOTERAPEUT INT,
    ID_JUCATOR INT,
    TIP_ACCIDENTARE VARCHAR2(50),
    DURATA_RECUPERARE VARCHAR2(50),
    FOREIGN KEY (ID_FIZIOTERAPEUT) REFERENCES FIZIOTERAPEUT(ID_FIZIOTERAPEUT),
    FOREIGN KEY (ID_JUCATOR) REFERENCES JUCATOR(ID_JUCATOR),
    CONSTRAINT TIP_ACCIDENTARE_CHECK CHECK (UPPER(TIP_ACCIDENTARE) IN ('LEZIUNE LIGAMENT INCRUCISAT ANTERIOR', 'LEZIUNE LIGAMENT COLATERAL MEDIAL', 'RUPTURA DE MENISC')),
    CONSTRAINT DURATA_RECUPERARE_CHECK CHECK (UPPER(DURATA_RECUPERARE) LIKE '%_LUNI')
);

