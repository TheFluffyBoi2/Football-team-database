CREATE OR REPLACE PROCEDURE E6
IS 
    iterator VARCHAR2(1);
    v_nume_jucator JUCATOR.NUME%TYPE;
    TYPE tablou_indexat IS TABLE OF NUMBER INDEX BY VARCHAR2(1);
    t_vocale tablou_indexat;
    TYPE tablou_impricat IS TABLE OF JUCATOR.NUME%TYPE;
    t_nume tablou_impricat := tablou_impricat();
    TYPE vector IS VARRAY(5) OF JUCATOR.NUME%TYPE;
    v_pare vector := vector('A', 'E', 'I', 'O', 'U');
BEGIN
    t_vocale('A') := 0;
    t_vocale('E') := 0;
    t_vocale('I') := 0;
    t_vocale('O') := 0;
    t_vocale('U') := 0;
    SELECT NUME BULK COLLECT INTO t_nume
    FROM JUCATOR
    WHERE POZITIE IN ('ST', 'CAM', 'LW', 'RW');
    DBMS_OUTPUT.PUT_LINE('Atacanti: ');
    FOR i IN t_nume.FIRST..t_nume.LAST LOOP
        FOR j IN v_pare.FIRST..v_pare.LAST LOOP
            IF SUBSTR(t_nume(i), 1, 1) = v_pare(j) THEN
                t_vocale(v_pare(j)) := t_vocale(v_pare(j)) + 1;
                EXIT;
            END IF;
        END LOOP;
        DBMS_OUTPUT.PUT_LINE(t_nume(i));
    END LOOP;
    DBMS_OUTPUT.PUT_LINE('-----------------------');

    iterator := t_vocale.FIRST;
    WHILE iterator IS NOT NULL LOOP
        DBMS_OUTPUT.PUT_LINE('Numarul de atacanti al caror nume incepe cu vocala ' || iterator || ' este de ' || t_vocale(iterator));
        iterator := t_vocale.NEXT(iterator);
    END LOOP;
END E6;

BEGIN
    E6;
END;
/



