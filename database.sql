CREATE OR REPLACE PROCEDURE E7(v_liga CLUB_DE_FOTBAL.LIGA%TYPE)
IS
    v_id JUCATOR.ID_JUCATOR%TYPE;
    v_nume JUCATOR.NUME%TYPE;
    v_prenume JUCATOR.PRENUME%TYPE;
    v_salariu JUCATOR.SALARIU%TYPE;

    v_cnt NUMBER;

    CURSOR cursor_jucatori IS
        SELECT J.ID_JUCATOR AS ID, J.NUME AS NUME, J.PRENUME AS PRENUME, J.SALARIU AS SALARIU
        FROM JUCATOR J
        JOIN CLUB_DE_FOTBAL CF ON J.ID_CLUB = CF.ID_CLUB
        WHERE CF.LIGA = v_liga
        AND J.SALARIU <= 300000;

    CURSOR cursor_accidentari(v_id JUCATOR.ID_JUCATOR%TYPE) IS
        SELECT R.TIP_ACCIDENTARE AS TIP_ACCIDENTARE, R.DURATA_RECUPERARE AS DURATA
        FROM RECUPERARE R
        WHERE R.ID_JUCATOR = v_id;
BEGIN
    OPEN cursor_jucatori;
    LOOP
        FETCH cursor_jucatori INTO v_id, v_nume, v_prenume, v_salariu;
        EXIT WHEN cursor_jucatori%NOTFOUND;
        DBMS_OUTPUT.PUT_LINE('Jucatorul ' || v_nume || ' ' || v_prenume || ' cu salariu de ' || v_salariu || ' a suferit urmatoarele accidentari grave: ');
        v_cnt := 0;
        FOR i IN cursor_accidentari(v_id) LOOP
            IF TO_NUMBER(SUBSTR(i.DURATA, 1, INSTR(i.DURATA, ' ') - 1)) >= 5 THEN
                DBMS_OUTPUT.PUT_LINE(i.TIP_ACCIDENTARE || ', recuperarea necesitand ' || i.DURATA);
                v_cnt := v_cnt + 1;
            END IF;
        END LOOP;
        IF v_cnt = 0 THEN
            DBMS_OUTPUT.PUT_LINE('Jucatorul nu a suferit accidentari grave');
            UPDATE JUCATOR
            SET SALARIU = SALARIU + 5000
            WHERE ID_JUCATOR = v_id;
            COMMIT;
        END IF;
        DBMS_OUTPUT.PUT_LINE('--------------------------------------');
    END LOOP;
    CLOSE cursor_jucatori;
END E7;
/

BEGIN
    DBMS_OUTPUT.PUT_LINE('Premier League: ');
    E7('Premier League');
    DBMS_OUTPUT.PUT_LINE('--------------------------------------');

    DBMS_OUTPUT.PUT_LINE('La Liga: ');
    E7('La Liga');
    DBMS_OUTPUT.PUT_LINE('--------------------------------------');
END;
