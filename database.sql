CREATE OR REPLACE PROCEDURE E9(v_durata ANTRENAMENT.DURATA%TYPE, v_nume_club CLUB_DE_FOTBAL.NUME%TYPE)
IS
    v_nr_antrenamente NUMBER := 0;
    v_salariu JUCATOR.SALARIU%TYPE;
    v_id_club CLUB_DE_FOTBAL.ID_CLUB%TYPE;
    DURATA_PREA_MICA EXCEPTION;
    NU_EXISTA_ANTRENAMENTE EXCEPTION;
BEGIN
    IF v_durata < 15 THEN
        RAISE DURATA_PREA_MICA;
    END IF;

    SELECT ID_CLUB INTO v_id_club
    FROM CLUB_DE_FOTBAL
    WHERE NUME = v_nume_club;

    SELECT COUNT(*) INTO v_nr_antrenamente
    FROM JUCATOR J
    JOIN ANTRENAMENT A ON A.ID_JUCATOR = J.ID_JUCATOR
    WHERE J.ID_CLUB = v_id_club;

    IF v_nr_antrenamente = 0 THEN
        RAISE NU_EXISTA_ANTRENAMENTE;
    END IF;

    FOR i IN (
        SELECT J.ID_JUCATOR AS ID, CDF.NUME AS NUME_CLUB, CA.NUME AS NUME_CENTRU, A.DURATA AS DURATA
        FROM JUCATOR J
        JOIN CLUB_DE_FOTBAL CDF ON J.ID_CLUB = CDF.ID_CLUB
        JOIN CENTRU_ANTRENAMENT_CLUB CAC ON CDF.ID_CLUB = CAC.ID_CLUB
        JOIN CENTRU_ANTRENAMENT CA ON CAC.ID_CENTRU_DE_ANTRENAMENT = CA.ID_CENTRU_DE_ANTRENAMENT
        JOIN ANTRENAMENT A ON CA.ID_CENTRU_DE_ANTRENAMENT = A.ID_CENTRU_DE_ANTRENAMENT  
        WHERE A.DURATA >= v_durata AND CDF.ID_CLUB = v_id_club AND A.ID_JUCATOR = J.ID_JUCATOR
    ) LOOP
        DBMS_OUTPUT.PUT_LINE(
            'ID Jucator: ' || i.ID ||
            ', Club: ' || i.NUME_CLUB ||
            ', Centru de antrenament: ' || i.NUME_CENTRU ||
            ', Durata: ' || i.DURATA
        );
    END LOOP;
    
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20005, 'Nu exista un club cu acest nume');
    WHEN DURATA_PREA_MICA THEN
        RAISE_APPLICATION_ERROR(-20006, 'Durata introdusa este prea mica');
    WHEN NU_EXISTA_ANTRENAMENTE THEN
        RAISE_APPLICATION_ERROR(-20007, 'Jucatorii clubului specificat nu au participat la antrenamente');
END E9;
/

